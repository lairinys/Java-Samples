    public void configurar(ViewMenuSecundario secundario,String tipo){
        
        secundario.lblTitulo.setText("Gestionar "+tipo);
        secundario.btnModificar.setText("Modificar "+tipo);
        secundario.btnMostrar.setText("Mostrar "+tipo);
        secundario.btnRegistrar.setText("Registrar "+tipo);
        secundario.btnEliminar.setText("Eliminar "+tipo);
        secundario.setLocationRelativeTo(null);
    }
     
    
